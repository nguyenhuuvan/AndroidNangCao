package com.assmob201.poly.assignment.model;

public class IntroCourse {
    String ID;
    String corseID;
    String classs;
    String date;
    String phong;
    String ca;
    public IntroCourse() {

    }

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getCorseID() {
        return corseID;
    }

    public void setCorseID(String corseID) {
        this.corseID = corseID;
    }

    public String getClasss() {
        return classs;
    }

    public void setClasss(String classs) {
        this.classs = classs;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getPhong() {
        return phong;
    }

    public void setPhong(String phong) {
        this.phong = phong;
    }

    public String getCa() {
        return ca;
    }

    public void setCa(String ca) {
        this.ca = ca;
    }
}

